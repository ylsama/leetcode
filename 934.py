from typing import List
from queue import PriorityQueue

class Solution:
    def getAdjNode(self, x, y):
        result = []
        dx, dy = 0, 1
        for i in range(4):
            nx , ny = x + dx, y + dy 
            if 0 <= nx < self.n and 0 <= ny < self.n:
                result.append((nx, ny))
            if dx == 0:
                dx, dy = dy, 0
            else:
                dx, dy = 0, - dx
        return result

    def shortestBridge(self, grid: List[List[int]]) -> int:
        self.n = len(grid)
        self.grid = grid
        n = self.n
        trace = [[-1 for i in range(n)] for i in range(n)]
        start_r, start_c = 0, 0

        check = False
        for i in range(n):
            if check:
                break
            for j in range(n):
                if grid[i][j] == 1:
                    start_r, start_c = i, j 
                    check = True
                    break
        q = []
        q.append((start_r, start_c))
        island_1 = []
        island_1.append((0, (start_r, start_c)))
        while len(q) > 0:
            x, y = q.pop(0)
            trace[x][y] = 0
            adjNode = self.getAdjNode(x,y)
            for nx, ny in adjNode:
                if self.grid[nx][ny] == 1 and trace[nx][ny] == -1:
                    q.append((nx, ny))
                    # optimictic
                    trace[nx][ny] = 0
                    island_1.append((0, (nx, ny)))
        while island_1.__len__() > 0:
            v, (x, y) = island_1.pop(0)
            if trace[x][y] != 0 and self.grid[x][y] == 1:
                return v-1
            adjNode = self.getAdjNode(x,y)
            for nx, ny in adjNode:
                if trace[nx][ny] < 0:
                    # optimictic
                    trace[nx][ny] = v+1
                    island_1.append((v+1, (nx, ny)))
        result = n
        return result

